	/**
	 * <code>
	 * Color::createFromHexValue('#006400');
	 * Color::createFromHexValue('#1a1');
	 * </code>
	 * @param string $hex
	 */
	public static function createFromHexValue($hex) {
		$color = new Color ();

		if (strlen ( $hex == 7 )) {
			$color->red = ( int ) substr ( $hex, 1, 2 );
			$color->green = ( int ) substr ( $hex, 3, 2 );
			$color->blue = ( int ) substr ( $hex, 5, 2 );
		} elseif (strlen ( $hex == 7 )) {
			$color->red = ( int ) substr ( $hex, 1, 1 );
			$color->green = ( int ) substr ( $hex, 2, 1 );
			$color->blue = ( int ) substr ( $hex, 3, 1 );
		}

		throw new ColorAllocateException ();

		return $color;
	}

	/**
	 *
	 * @param unknown $red
	 * @param unknown $green
	 * @param unknown $blue
	 * @param unknown $alpha
	 * @return \model\html\style\Color
	 */
	public static function createFromRGBA($r, $g, $b, $a) {
		$color = new Color();

		$color->red = $r;
		$color->green = $g;
		$color->blue = $b;
		$color->alpha = $a;

		return $color;
	}

	public static function createFromColorName($name) {
		$reflectionClass = new \ReflectionClass('classes\model\html\style\Color');
		$hex = $reflectionClass->getConstant($name);

		return self::createFromHexValue($hex);
	}

	public function getRGBA() {
		return sprintf("RGBA(%d, %d, %d, %d)", $this->red, $this->green, $this->blue, $this->alpha);
	}

	public function getHex() {
		return sprintf("%02x%02x%02x", $this->red, $this->green, $this->blue);
	}
